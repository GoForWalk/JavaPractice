package practice.cardcompany;

public class Card {
	
	private int cardNumber;
	
	public Card() {
		super();
	}
	

	public Card(int cardNumber) {
		super();
		this.cardNumber = cardNumber;
	}


	public int getCardNumber() {
		return cardNumber;
	}

	public void setCardNumber(int cardNumber) {
		this.cardNumber = cardNumber;
	}
	
} // end class
