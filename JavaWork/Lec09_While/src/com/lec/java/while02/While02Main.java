package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		
		
		//구구단 2단
		// while 사용
		// 구구단 2단의 while 문으로 작성
	
		int i = 1;
		while(i <= 9) {
			System.out.println("2 X " + i + " = " + (i * 2));
			i++;
		}


	} // end main()

} // end class While02Main









